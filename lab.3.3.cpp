#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    double x;  // вхідний аргумент
    double y;  // результат обчислення

    cout << "x = ";  cin >> x;

    // Визначаємо функцію на різних інтервалах
    if (x <= -4) {
        y = -2;  // При x <= -4, y = -2
    }
    else if (x > -4 && x <= 0) {
        y = (x + 4) / 2 - 2;  // Лінійна функція на інтервалі (-4, 0]
    }
    else if (x > 0 && x <= 2) {
        y = x * x;  // Парабола y = x^2 на інтервалі (0, 2]
    }
    else if (x > 2 && x <= 10) {
        y = -0.5 * x + 5;  // Лінійна функція на інтервалі (2, 10]
    }
    else {
        y = 0;  // Значення за межами визначеного графіку
        cout << "x виходить за межі графіка!" << endl;
    }

    // Виведення результату
    cout << "y = " << y << endl;

    return 0;
}